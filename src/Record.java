
public class Record {
	private int rank;
	private int marks;
	private int levels;
	
	public Record(int rank,int marks,int levels)
	{
		this.rank = rank;
		this.marks =marks;
		this.levels =levels;
	}
	
	public int getrank()
	{
		return rank;
	}
	
	public int getmarks()
	{
		return marks;
	}
	
	public int getlevels()
	{
		return levels;
	}
	
	
}
